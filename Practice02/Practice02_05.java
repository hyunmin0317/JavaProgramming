package Practice02;

public class Practice02_05 {
	public static void main(String[] args) {
		int opr = 0;
		opr += 3;
		System.out.println(opr++);
		System.out.println(opr);
		System.out.println(++opr);
		System.out.println(opr);
		System.out.println(opr--);
		System.out.println(opr);
		System.out.println(--opr);
		System.out.println(opr);
	}
}